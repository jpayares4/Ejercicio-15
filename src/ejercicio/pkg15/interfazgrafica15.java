    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String res1,res2,res3;
        double n1;
        double op1,op2,op3;
        
                
        n1 = Double.parseDouble(prestamo.getText());
        
        if (n1 == 0 ){
             JOptionPane.showMessageDialog(this,"Digita al menos un numero distinto a cero(0)","Error",JOptionPane.ERROR_MESSAGE);
             borrar();
            prestamo.requestFocusInWindow();
        }
        else{
            
       
       
        
        op1 =n1/0.24;
        op2 = (op1/2)/4;
        op3 = (op1/2)/20;
        
        res1 = String.valueOf(op2);
        res2 = String.valueOf(op3);
     
        cuota1.setText(res1);
        cuota2.setText(res2);
        
         }
    }//GEN-LAST:event_jButton1ActionPerformed

